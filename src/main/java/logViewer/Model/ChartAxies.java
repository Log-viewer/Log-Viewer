package logViewer.Model;

public class ChartAxies {
    private String x;
    private String y;

    public ChartAxies() {}

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
